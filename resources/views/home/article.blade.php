                <h2 itemprop="name headline" class="title">如何治好你的颈椎病 程序猿</h2>
                <div class="p_time"><i class="fa fa-sun-o"></i>&nbsp;&nbsp;2016-11-06<i class="fa fa-empire"></i>&nbsp;&nbsp;<ul class="post-categories">
                        <li><a href="http://jianbi.me/category/note" rel="category tag">札记</a></li></ul></div>
                <article class="post single" itemscope itemtype="http://schema.org/BlogPosting">
                    <p>程序员天天对着电脑，眼睛，颈椎等等，都会落下不少的职业病。来说说怎么治疗自己的颈椎病。</p>
                    <p>一、颈椎病是怎么产生的</p>
                    <p>形成颈椎病的核心原因是：不良生活习惯</p>
                    <p>我们身体的绝大部分疾病都是来自不良的生活习惯，生活习惯不改，再厉害的治疗也只能是缓解，无法根除。</p>
                    <p>影响形成颈椎病的生活习惯有那些呢？</p>
                    <p>1、不良坐姿习惯：天天长时间弯腰低头看文件看电脑，颈椎不出问题才怪，同时也容易驼背和腰疼。你侧着站到镜子前，看看你的脖子是脊椎是直的么？</p>
                    <p>2、呼吸太浅习惯：天天低头窝胸，呼吸就会变的很浅，呼吸变浅，人身体需要的氧气就供应不足，身体精气神就变的很弱。时间长了，颈椎部分的经络就不通常，酸痛就会出现了。</p>
                    <p>3、睡眠姿势习惯：一个人每天三分之一以上时间都是在床上度过的，甚至许多人喜欢靠着枕头窝在床上上网或者看书，这时候就形成两个严重的不良习惯。</p>
                    <p>坐在床上上网或者看书：刚开始身体还能直，一会儿就索下去了，最后剩一个头和弯在那里，气就更不通畅了，缺氧自然了，加上看书自然犯困，困了之后，就睡着了，自然越来越严重。</p>
                    <p>睡眠用的枕头非常不合适：颈椎酸痛的人，睡觉都喜欢用高的枕头，以适应自己长期形成的姿势。这样的结果是，头很高，所有的力量都压在颈椎上，并且颈椎还是凸出和弯曲的，不严重才怪。</p>
                    <p>如果你的颈椎经常不舒服，你现在就用以上三方面对照一下自己，看看你是不是也是这样的，如果差不多，那我下面的方法就对你非常的有效了。</p>
